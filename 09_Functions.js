//Basic Function (No Parameters, No Return):
function greet() {
    console.log("Hello World");
}

greet();

//Function with Parameters:
function add(a, b) {
    console.log(a + b);
}

add(6, 4);

//Function with Return Value:
function multiplication(x, y) {
    return x * y;
}

console.log(multiplication(6, 5));